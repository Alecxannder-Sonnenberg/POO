public class Usuario {
	
	int idUsuario;
	String nome;
	String email;
	String telefone;
	String emprego;
	
	String dataSalario;
	
	double rendaFixa;

	public int getIdUsuario() {
		return idUsuario;
	}

	public void setIdUsuario(int idUsuario) {
		this.idUsuario = idUsuario;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTelefone() {
		return telefone;
	}

	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}

	public String getEmprego() {
		return emprego;
	}

	public void setEmprego(String emprego) {
		this.emprego = emprego;
	}

	public String getDataSalario() {
		return dataSalario;
	}

	public void setDataSalario(String dataSalario) {
		this.dataSalario = dataSalario;
	}

	public double getRendaFixa() {
		return rendaFixa;
	}

	public void setRendaFixa(double rendaFixa) {
		this.rendaFixa = rendaFixa;
	}
	


	
}
